package org.catrobat.confluence.rest.json;

import org.catrobat.confluence.activeobjects.Resource;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

@SuppressWarnings("unused")
@XmlRootElement
@XmlAccessorType(XmlAccessType.FIELD)
public final class JsonResource {

    @XmlElement
    private String resourceName;
    @XmlElement
    private String groupName;

    public JsonResource() {

    }

    public JsonResource(final Resource toCopy) {
        this.resourceName = toCopy.getResourceName();
        this.groupName = toCopy.getGroupName();
    }

    public String getResourceName() {
        return resourceName;
    }

    public void setResourceName(String resourceName) {
        this.resourceName = resourceName;
    }

    public String getGroupName() {
        return groupName;
    }

    public void setGroupName(String groupName) {
        this.groupName = groupName;
    }
}
